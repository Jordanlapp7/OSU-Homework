# Description: Asks the user for a temperature in Celsius and converts to Fahrenheit.

print("Please enter a Celsius temperature.")
celsius = float(input())
fahrenheit = (9.0 / 5.0) * celsius + 32
print("The equivalent Fahrenheit temperature is:")
print(fahrenheit)
